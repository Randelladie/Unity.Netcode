    public static NativeList<byte> ToNativeList(byte[] data, Allocator allocator)
    {
        var list = new NativeList<byte>(allocator);

        using (var temp = new NativeArray<byte>(data, Allocator.Temp))
        {
            list.AddRange(temp);
        }

        return list;
    }


    // --- Payload Management

    void AppendPayload(int connectionId, byte[] data)
    {
        if (connectionId == 1)
        {
            var native = new NativeArray<byte>(data, Allocator.Temp);
            OnBytesReceived?.Invoke(native);
            return;
        }

        if (data == null || data.Length == 0)
            return;

        if (!bufferPayloads.TryGetValue(connectionId, out var buffer) || buffer == null || buffer.Length == 0)
        {
            // first payload = just store it
            bufferPayloads[connectionId] = data;
            return;
        }

        // merge old + new
        byte[] merged = new byte[buffer.Length + data.Length];

        Buffer.BlockCopy(buffer, 0, merged, 0, buffer.Length);
        Buffer.BlockCopy(data, 0, merged, buffer.Length, data.Length);

        bufferPayloads[connectionId] = merged;
    }

    void AppendReliablePayload(int connectionId, byte[] data)
    {
        if (connectionId == 1)
        {
            var native = new NativeArray<byte>(data, Allocator.Temp);
            OnBytesReceived?.Invoke(native);
            return;
        }

        if (data == null || data.Length == 0)
            return;

        if (!reliableBufferPayloads.TryGetValue(connectionId, out var buffer) || buffer == null || buffer.Length == 0)
        {
            // first payload = just store it
            reliableBufferPayloads[connectionId] = data;
            return;
        }

        // merge old + new
        byte[] merged = new byte[buffer.Length + data.Length];

        Buffer.BlockCopy(buffer, 0, merged, 0, buffer.Length);
        Buffer.BlockCopy(data, 0, merged, buffer.Length, data.Length);

        reliableBufferPayloads[connectionId] = merged;
    }

    void AppendAllPayloadsExcept(int connectionId, byte[] data)
    {
        foreach (var kvp in payloads)
        {
            if (kvp.Key == connectionId) continue;
            AppendPayload(kvp.Key, data);
        }
    }

    void AppendAllReliablePayloadsExcept(int connectionId, byte[] data)
    {
        foreach (var kvp in payloads)
        {
            if (kvp.Key == connectionId) continue;
            AppendReliablePayload(kvp.Key, data);
        }
    }

    void AppendAllPayloads(byte[] data)
    {
        foreach (var kvp in payloads)
        {
            AppendPayload(kvp.Key, data);
        }
    }

    void ClearPayload(int connectionId)
    {
        payloads[connectionId] = new byte[] { };
    }
    
    bool AreAllPayloadsEmpty()
    {
        bool allEmpty = true;

        foreach (var kvp in payloads)
        {
            if (kvp.Value.Length > 0)
            {
                allEmpty = false;
                break;
            }
        }
        return allEmpty;
    }

    bool AreAllReliablePayloadsEmpty()
    {
        bool allEmpty = true;

        foreach (var kvp in reliablePayloads)
        {
            if (kvp.Value.Length > 0)
            {
                allEmpty = false;
                break;
            }
        }
        return allEmpty;
    }

    void AppendBufferPayloads()
    {
        foreach (var kvp in bufferPayloads)
        {
            int key = kvp.Key;
            byte[] buffer = kvp.Value;

            if (buffer == null || buffer.Length == 0)
                continue;

            // FIRST TIME: just move buffer into payload
            if (!payloads.TryGetValue(key, out var main) || main == null || main.Length == 0)
            {
                payloads[key] = buffer;
                continue;
            }

            // APPEND: merge arrays
            byte[] merged = new byte[main.Length + buffer.Length];

            Buffer.BlockCopy(main, 0, merged, 0, main.Length);
            Buffer.BlockCopy(buffer, 0, merged, main.Length, buffer.Length);

            payloads[key] = merged;
        }

        // CLEAR buffers (replace with empty arrays or null)
        var keys = new List<int>(bufferPayloads.Keys);
        foreach (var key in keys)
        {
            bufferPayloads[key] = Array.Empty<byte>();
        }

        // === RELIABLE ===

        foreach (var kvp in reliableBufferPayloads)
        {
            int key = kvp.Key;
            byte[] buffer = kvp.Value;

            if (buffer == null || buffer.Length == 0)
                continue;

            // FIRST TIME: just move buffer into payload
            if (!reliablePayloads.TryGetValue(key, out var main) || main == null || main.Length == 0)
            {
                reliablePayloads[key] = buffer;
                continue;
            }

            // APPEND: merge arrays
            byte[] merged = new byte[main.Length + buffer.Length];

            Buffer.BlockCopy(main, 0, merged, 0, main.Length);
            Buffer.BlockCopy(buffer, 0, merged, main.Length, buffer.Length);

            reliablePayloads[key] = merged;
        }

        // CLEAR buffers (replace with empty arrays or null)
        var reliableKeys = new List<int>(reliableBufferPayloads.Keys);
        foreach (var key in reliableKeys)
        {
            reliableBufferPayloads[key] = Array.Empty<byte>();
        }
    }

    void AppendBufferPackets()
    {
        packets.AddRange(bufferPackets);
        bufferPackets.Clear();
        reliablePackets.AddRange(reliableBufferPackets);
        reliableBufferPackets.Clear();
    }


    // --- RECEIVE ---

    void HostReceiveData()
    {
        DataStreamReader stream;
        NetworkEvent.Type evt;
        foreach (var kvpConnection in connections)
        {
            var targetKey = kvpConnection.Key;
            var targetConnection = kvpConnection.Value;
            if (!targetConnection.IsCreated) continue;

            while ((evt = targetConnection.PopEvent(driver, out stream)) != NetworkEvent.Type.Empty)
            {
                switch (evt)
                {
                    case NetworkEvent.Type.Data:
                        // Allocate a NativeArray of the correct length
                        NativeArray<byte> receivedData = new NativeArray<byte>(stream.Length, Allocator.Temp);

                        // Copy bytes from the DataStreamReader into the NativeArray
                        stream.ReadBytes(receivedData);
                        HostHandleReceivedData(receivedData);
                        break;
                    case NetworkEvent.Type.Connect:
                        //Debug.Log("Player Connected!");
                        //OnConnected?.Invoke(targetKey);
                        break;
                    case NetworkEvent.Type.Disconnect:
                        connection = default; // mark connection as disconnected
                        Debug.Log("Player Disconnected!");
                        OnDisconnected?.Invoke(targetKey);
                        break;
                }
            }
        }
    }

    void HostHandleReceivedData(NativeArray<byte> data)
    {
        // Get Byte Intent
        // 1 = Set ConnectionId
        // 2 = OnByteReceived
        byte intent = data[0];
        int payloadSize = data.Length - 1;
        NativeArray<byte> payload =
            new NativeArray<byte>(payloadSize, Allocator.Temp);
        NativeArray<byte>.Copy(
            data,
            1,
            payload,
            0,
            payloadSize
        );

        switch (intent)
        {
            case 1:
                DataStreamReader reader = new DataStreamReader(payload);
                reader.ReadInt();
                break;
            case 2:
                HostHandlePacket(payload);
                break;
            default:
                break;
        }
    }

    void HostHandlePacket(NativeArray<byte> payload)
    {
        var reader = new DataStreamReader(payload);
        PacketCodec.Packet packet;
        // int loopCounter = 0;
        while (PacketCodec.ReadNext(ref reader, out packet))
        {
            if (packet.payload == null || packet.payload.Length == 0)
                continue;
            int loopCounter = 0;
            string message = "";
            DataStreamReader payloadReader = new DataStreamReader(new NativeArray<byte>(packet.payload, Allocator.Temp));
            while (payloadReader.Length - payloadReader.GetBytesRead() > 0)
            {
                if (loopCounter >= 1000)
                {
                    print("LOOP DETECTED IN NETWORK MANAGER");
                    break;
                }
                loopCounter++;
                message += $"[{payloadReader.ReadByte()}]";
            }
            //print($"Packet connectionId: {packet.connectionId} type: {packet.type} message: {message}");
            switch (packet.type)
            {
                case 1:
                    AppendPayload(packet.connectionId, packet.payload);
                    break;

                case 2:
                    AppendAllPayloads(packet.payload);
                    break;

                case 3:
                    AppendAllPayloadsExcept(packet.connectionId, packet.payload);
                    break;
            }
        }
    }

    void ClientReceiveData()
    {
        if (!connection.IsCreated) return;

        DataStreamReader stream;
        NetworkEvent.Type evt;

        while ((evt = connection.PopEvent(driver, out stream)) != NetworkEvent.Type.Empty)
        {
            switch (evt)
            {
                case NetworkEvent.Type.Data:
                    // Allocate a NativeArray of the correct length
                    NativeArray<byte> receivedData = new NativeArray<byte>(stream.Length, Allocator.Temp);

                    // Copy bytes from the DataStreamReader into the NativeArray
                    stream.ReadBytes(receivedData);
                    ClientHandleReceivedData(receivedData);
                    break;
                case NetworkEvent.Type.Connect:
                    Debug.Log("Connected to host!");
                    OnConnected?.Invoke(0);
                    break;
                case NetworkEvent.Type.Disconnect:
                    Debug.Log("Disconnected from host!");
                    OnDisconnected?.Invoke(0);
                    break;
            }
        }
    }

    void ClientHandleReceivedData(NativeArray<byte> data)
    {
        // Get Byte Intent
        // 1 = Set ConnectionId
        // 2 = OnByteReceived
        byte intent = data[0];
        int payloadSize = data.Length - 1;
        NativeArray<byte> payload =
            new NativeArray<byte>(payloadSize, Allocator.Temp);
        NativeArray<byte>.Copy(
            data,
            1,
            payload,
            0,
            payloadSize
        );

        switch (intent)
        {
            case 1:
                DataStreamReader reader = new DataStreamReader(payload);
                connectionId = reader.ReadInt();
                Debug.Log($"ConnectionId is now: {connectionId}");
                break;
            case 2:
                OnBytesReceived?.Invoke(payload);
                break;
            default:
                break;
        }

    }


    // --- SEND ---

    void HostSendPayloads()
    {
        if (!driver.IsCreated)
            return;

        if (!AreAllPayloadsEmpty())
        {
            // Snapshot keys only (NOT deep copy of data)
            var keys = new List<int>(payloads.Keys);

            foreach (var key in keys)
            {
                if (!payloads.TryGetValue(key, out var data) || data.Length == 0)
                    continue;

                if (key == connectionId)
                {
                    var native = new NativeArray<byte>(data, Allocator.Temp);
                    OnBytesReceived?.Invoke(native);
                }
                else
                {
                    connections.TryGetValue(key, out var targetConnection);

                    if (driver.BeginSend(targetConnection, out var writer) == 0)
                    {
                        writer.WriteByte(2);
                        writer.WriteBytes(data);
                        driver.EndSend(writer);
                    }
                }

                // Clear AFTER sending (important change)
                payloads[key] = new byte[] { };
            }
        }

        if (!AreAllReliablePayloadsEmpty())
        {
            // Snapshot keys only (NOT deep copy of data)
            var keys = new List<int>(reliablePayloads.Keys);

            foreach (var key in keys)
            {
                if (!reliablePayloads.TryGetValue(key, out var data) || data.Length == 0)
                    continue;

                if (key == connectionId)
                {
                    var native = new NativeArray<byte>(data, Allocator.Temp);
                    OnBytesReceived?.Invoke(native);
                }
                else
                {
                    connections.TryGetValue(key, out var targetConnection);

                    if (driver.BeginSend(reliablePipeline, targetConnection, out var writer) == 0)
                    {
                        writer.WriteByte(2);
                        writer.WriteBytes(data);
                        driver.EndSend(writer);
                    }
                }

                // Clear AFTER sending (important change)
                reliablePayloads[key] = new byte[] { };
            }
        }
    }

    void ClientSendPayload()
    {
        if (!driver.IsCreated)
            return;

        if (connection == default)
            return;

        if (packets.Count != 0)
        {

            if (driver.BeginSend(connection, out var writer) != 0)
                return;

            writer.WriteByte(2);

            foreach (var packet in packets)
            {
                // print($"packet type: {packet.type} packet connectionId: {packet.connectionId}");
                if (!(packet.payload != null && packet.payload.Length > 0) || packet.payload.Length == 0)
                    continue;

                switch (packet.type)
                {
                    case 1:
                        PacketCodec.WriteSend(ref writer, packet.connectionId, packet.payload);
                        break;

                    case 2:
                        PacketCodec.WriteSendToAll(ref writer, packet.payload);
                        break;

                    case 3:
                        PacketCodec.WriteSendToAllExcept(ref writer, packet.connectionId, packet.payload);
                        break;
                }
            }

            driver.EndSend(writer);

            packets.Clear();
        }

        if (reliablePackets.Count != 0)
        {

            if (driver.BeginSend(reliablePipeline, connection, out var writer) != 0)
                return;

            writer.WriteByte(2);

            foreach (var packet in reliablePackets)
            {
                // print($"packet type: {packet.type} packet connectionId: {packet.connectionId}");
                if (!(packet.payload != null && packet.payload.Length > 0) || packet.payload.Length == 0)
                    continue;

                switch (packet.type)
                {
                    case 1:
                        PacketCodec.WriteSend(ref writer, packet.connectionId, packet.payload);
                        break;

                    case 2:
                        PacketCodec.WriteSendToAll(ref writer, packet.payload);
                        break;

                    case 3:
                        PacketCodec.WriteSendToAllExcept(ref writer, packet.connectionId, packet.payload);
                        break;
                }
            }

            driver.EndSend(writer);

            reliablePackets.Clear();
        }
    }


    // --- SPECIAL ---

    void AcceptClients()
    {
        NetworkConnection newConnection;
        while ((newConnection = driver.Accept()) != default(NetworkConnection))
        {

            int connectionId = nextConnectionId;
            nextConnectionId++;
            connections.Add(connectionId, newConnection);

            if (driver.BeginSend(reliablePipeline, newConnection, out var writer) == 0)
            {
                writer.WriteByte(1); // Intent
                writer.WriteInt(connectionId);
                writer.WriteShort((short)connections.Count);
                foreach (var kvp in connections)
                {
                    writer.WriteInt(kvp.Key);
                }
                writer.WriteInt(connectionId);
                driver.EndSend(writer);
                Debug.Log("Client connected! Total clients: " + connections.Count);
                OnConnected?.Invoke(connectionId);
            }
            else
            {
                Debug.LogWarning($"Failed to send connection ID to client {connectionId}");
            }
        }
    }

    void Shutdown()
    {
        if (driver.IsCreated)
        {
            driver.Dispose();
        }
    }

    void SyncPayloadsWithConnections()
    {
        if (!IsHost) return;

        // Remove payloads for disconnected clients
        List<int> toRemove = new();

        foreach (var kvp in payloads)
        {
            if (kvp.Key == 1) continue;
            if (!connections.ContainsKey(kvp.Key))
            {
                // handle join
                toRemove.Add(kvp.Key);
            }
        }

        foreach (int id in toRemove)
        {
            payloads.Remove(id);
        }

        // Ensure every connection has a payload buffer
        foreach (var connection in connections)
        {
            int id = connection.Key;

            if (!payloads.ContainsKey(id))
            {
                payloads[id] = Array.Empty<byte>();
            }
        }

        if (!payloads.ContainsKey(1))
        {
            payloads[1] = Array.Empty<byte>();
        }
    }

    void RemoveNonConnections()
    {
        List<int> disconnectedKeys = new List<int>();
        foreach (var kvp in connections)
        {
            int clientId = kvp.Key;
            NetworkConnection targetconnection = kvp.Value;
            if (!targetconnection.IsCreated)
            {
                // Mark disconnected clients for removal
                disconnectedKeys.Add(clientId);
            }
        }
        // Remove disconnected clients
        foreach (var key in disconnectedKeys)
        {
            connections.Remove(key);
        }
    }

    bool StartHosting(ushort port)
    {
        if (driver.Bind(NetworkEndpoint.AnyIpv4.WithPort(port)) != 0)
        {
            Debug.LogError("Host failed to bind");
            return false;
        }

        if (driver.Listen() != 0)
        {
            Debug.LogError("Host failed to listen");
            return false;
        }

        Debug.Log($"Host is now listening on port {port}");

        connectionId = 1;
        IsHost = true;

        return true;
    }

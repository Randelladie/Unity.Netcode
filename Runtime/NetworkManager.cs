    /// <summary>
    /// Connects directly to a host using the specified IP address and port.
    /// This overload bypasses any previously stored connection settings.
    /// </summary>
    /// <param name="ipAddress">The IP address of the host to connect to.</param>
    /// <param name="port">The network port on which the host is listening.</param>
    public async Task ConnectIP(string ipAddress, ushort port)
    {
        var settings = new NetworkSettings();

        driver = NetworkDriver.Create(settings);
        reliablePipeline = driver.CreatePipeline(typeof(ReliableSequencedPipelineStage));

        // Parse IP
        var endpoint = NetworkEndpoint.Parse(ipAddress, port);

        connection = driver.Connect(endpoint);

        Debug.Log($"Client connecting to {ipAddress}:{port} ...");

        IsClient = true;

        await Task.CompletedTask;
    }

    /// <summary>

    /// <summary>
    /// Creates a Unity Relay allocation, retrieves a join code, configures the transport, and starts listening for incoming client connections. The local player becomes the host/server. 
    /// </summary>
    public async void Host()
    {
        RelayServerData rsd = await relayHandler.HostRelay();
        var settings = new NetworkSettings();
        settings.WithRelayParameters(ref rsd);

        driver = NetworkDriver.Create(settings);
        reliablePipeline = driver.CreatePipeline(typeof(ReliableSequencedPipelineStage));

        if (!StartHosting(relayPort)) { return; }

        Debug.Log("Relay Host is now listening on port " + relayPort);
        joinCode = relayHandler.joinCode;
    }

    /// <summary>
    /// Starts a local host using Unity Transport without Relay.
    /// Creates a NetworkDriver, binds to the configured port,
    /// and begins listening for incoming client connections.
    /// </summary>
    public void HostLocal()
    {
        driver = NetworkDriver.Create();
        reliablePipeline = driver.CreatePipeline(typeof(ReliableSequencedPipelineStage));

        if (!StartHosting(localPort)) { return; }

        Debug.Log($"Host is now listening on port {localPort}");
    }

    /// <summary>
    /// Terminates the Relay allocation, disconnects all connected clients, stops accepting new connections, and clears host-side networking resources.
    /// </summary>
    public void StopHost()
    {
        // Disconnect all remote clients
        foreach (var connection in connections)
        {
            connection.Value.Disconnect(driver);
        }

        connections.Clear();

        // Close server socket
        driver.Dispose();

        // Reset state
        IsHost = false;
    }


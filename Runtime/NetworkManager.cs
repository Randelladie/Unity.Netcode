    #region Listeners
    void Awake()
    {
        DontDestroyOnLoad(gameObject); // Persist across scenes
    }

    void Start()
    {
        relayHandler.OnUnityServiceInitialized += () => OnUnityServiceInitialized?.Invoke();
        relayHandler.OnJoinCodeGenerated += (string val) => OnJoinCodeGenerated?.Invoke(val);
        relayHandler.OnRelayError += (string val) => OnRelayError?.Invoke(val);
    }

    void Update()
    {
        if (!driver.IsCreated)
            return;

        driver.ScheduleUpdate().Complete();

        if (enableNetworkUpdate)
        {
            networkDelta += Time.deltaTime;
            float interval = 1f / networkUpdatePerSecond;

            // FIXED TICK LOOP FIRST (deterministic network step)
            while (networkDelta >= interval)
            {
                networkDelta -= interval;
                NetworkUpdate();
            }
        }

        // RECEIVE ALWAYS AFTER DRIVER UPDATE
        if (IsHost)
        {
            HostReceiveData();
            AcceptClients();
        }

        if (IsClient)
        {
            ClientReceiveData();
        }
    }

    void OnDestroy()
    {
        Shutdown();
    }

    void OnApplicationQuit()
    {
        Shutdown();
    }
    #endregion
